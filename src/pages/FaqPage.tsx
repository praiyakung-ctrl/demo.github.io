function FaqItem({ index, q, a, open, onToggle }: { index: number; q: string; a: string; open: boolean; onToggle: () => void }) {
  return (
    <div className={`border-2 rounded-xl overflow-hidden transition-colors ${open ? 'border-navy-400 bg-white' : 'border-gray-200 bg-white'}`}>
      <button
        onClick={onToggle}
        aria-expanded={open}
        className="w-full flex items-center gap-4 px-4 py-4 text-left hover:bg-gray-50 transition-colors"
      >
        <span className={`w-10 h-10 rounded-full flex items-center justify-center text-xl font-extrabold flex-shrink-0 ${
          open ? 'bg-navy-700 text-white' : 'bg-navy-50 text-navy-700'
        }`}>
          {index + 1}
        </span>
        <span className="flex-1 text-2xl font-bold text-navy-700 leading-snug">{q}</span>
        <ChevronDown
          size={26}
          className={`flex-shrink-0 text-navy-400 transition-transform ${open ? 'rotate-180' : ''}`}
        />
      </button>
      {open && (
        <div className="px-4 pb-5 pl-[72px]">
          <p className="text-xl text-gray-700 leading-relaxed">{a}</p>
        </div>
      )}
    </div>
  );
}

